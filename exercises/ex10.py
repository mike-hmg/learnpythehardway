## Examples
print("I am 6'2\" tall")
# This escapes and prints the double quote in the middle of the string
print('I am 6\'2" tall')
# This excapes and prints the single quote in the middle of the string

tabby_cat = "\tI'm tabbed in." # Tabs the line in
persian_cat = "I'm split\non a line" # \n just splits the line with newline
backslash_cat = "I'm \\ a \\ cat." # doubling the \\ simply escapes one and prints one backslash

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""




print(tabby_cat)
print(persian_cat)
print(backslash_cat)
